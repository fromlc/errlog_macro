//------------------------------------------------------------------------------
// Divide
//------------------------------------------------------------------------------
class Divide {
public:
    int result;
    Divide(int x, int y) : result(0) {

        if (y == 0) {
            ERRLOG("divide by zero");
        }
        else {
            result = x / y;
        }
    }
};

